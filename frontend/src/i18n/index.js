const LOCALES = { en: EN, ar: AR };

const STORAGE_KEY = 'sga-lang';

/** Detect locale from localStorage → GCC timezone → default 'en' */
export const detectLocale = () => {
  try {
    const saved = localStorage.getItem(STORAGE_KEY);
    if (saved === 'ar' || saved === 'en') return saved;
  } catch (_) {}
  try {
    const tz = Intl.DateTimeFormat().resolvedOptions().timeZone;
    const gcc = ['Asia/Dubai','Asia/Riyadh','Asia/Kuwait','Asia/Qatar','Asia/Bahrain','Asia/Muscat'];
    if (gcc.includes(tz)) return 'ar';
  } catch (_) {}
  return 'en';
};

export const saveLocale = (locale) => {
  try { localStorage.setItem(STORAGE_KEY, locale); } catch (_) {}
};

/** Translate key in given locale. Falls back to EN, then to key itself. */
export const t = (locale, key) => {
  const dict = LOCALES[locale] || EN;
  return dict[key] !== undefined ? dict[key] : (EN[key] !== undefined ? EN[key] : '');
};

export { EN, AR };
export default t;
